package Leetcode;

import java.util.HashMap;
import java.util.Map;
import java.util.TreeMap;

public class problem43 {

    static class TimeMap{
        private Map<String, TreeMap<Integer,String>> map;
        public TimeMap(){
            map = new HashMap<>();
        }

        public void set(String key,String value,int timestamp){
            map.computeIfAbsent(key,k->new TreeMap<>()).put(timestamp,value);
        }

        public String get(String key,int timestamp){
            TreeMap<Integer,String> treemap = map.get(key);
            if(treemap==null){
                return "";
            }

            Map.Entry<Integer,String> entry = treemap.floorEntry(timestamp);
            return entry == null ? "" : entry.getValue();
        }


    }
    public static void main(String[] args) {

        TimeMap timeMap = new TimeMap();
        timeMap.set("foo", "bar", 1);
        System.out.println(timeMap.get("foo", 1)); // returns "bar"
        System.out.println(timeMap.get("foo", 3)); // returns "bar"
        timeMap.set("foo", "baz", 2);
        System.out.println(timeMap.get("foo", 2)); // returns "baz"
        System.out.println(timeMap.get("foo", 3)); // returns "baz"
    }
}
